import type { MenuItem } from '@pawhaven/shared/types/menus.schema';
import type { RouterItem } from '@pawhaven/shared/types/routers.schema';
import type { NavigateFunction } from 'react-router-dom';

export const menuTypes = {
  link: 'link',
  component: 'component',
} as const;

export type MenuType = (typeof menuTypes)[keyof typeof menuTypes];
export interface MenuRenderType {
  menuItems: MenuItem[];
  activePath?: string;
  navigate: NavigateFunction;
}

export interface RouterInfoType extends RouterItem {
  data: Record<string, unknown> | undefined;
  id: string;
  params: Record<string, unknown> | undefined;
  pathname: string;
}
export interface RootLayoutHeaderProps {
  menuItems: MenuItem[];
  navigate: NavigateFunction;
  currentRouterInfo?: RouterInfoType;
}
